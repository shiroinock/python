#!/usr/bin/python3
a = 5
b = 2
wa = a + b
sa = a - b
seki = a * b
sho1 = a // b
amari = a % b
sho2 = a / b
print(a, '＋', b, '=', wa, sep=' ')
print(a, '−', b, '=', sa, sep=' ')
print(a, '×', b, '=', seki, sep=' ')
print(a, '÷', b, '=', sho1, '余り', amari, sep=' ')
print(a, '÷', b, '=', sho2, sep=' ')

# 整数、5 と整数、2 の２つの数の計算を行なうプログラムを書きなさい。
